export type MessageKey = keyof typeof messages.ko;

export function t(locale: Locale, key: MessageKey, vars?: Record<string, string | number>): string {
  let text: string = messages[locale][key] ?? messages.ko[key] ?? key;
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      text = text.replace(`{${k}}`, String(v));
    }
  }
  return text;
}

export function formatKrwLocale(amount: number, locale: Locale) {
  const formatted = Math.round(amount).toLocaleString(locale === "ko" ? "ko-KR" : "en-US");
  return locale === "ko" ? `${formatted}원` : `₩${formatted}`;
}
